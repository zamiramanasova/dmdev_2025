package leetCode.weekTen;

import java.util.Scanner;

/**
 * Дано положительное целое число , описать функцию, возвращающую число n Набор бит в его двоичном
 * представлении (также известном как вес Хэмминга).
 * Пример 1:
 * Входные данные: n = 11
 * Выход: 3
 * Объяснение:
 * Входная двоичная строка 1011 содержит в общей сложности три установленных бита.
 * Пример 2:
 * Входные данные: n = 128
 * Выход: 1
 * Объяснение:
 * Входная двоичная строка 10000000 содержит всего один установленный бит.
 * Пример 3:
 * Входные данные: n = 2147483645
 * Выход: 30
 * Объяснение:
 * Входная двоичная строковая 1111111111111111111111111111101 имеет в общей сложности тридцать установленных битов.
 */
public class NumberOfBits {
    // Память:
    //Создаёт строку длиной примерно log₂(n) символов (до 32 бит для int)
    //Преобразование в char[] и парсинг чисел — ещё немного памяти
    //⏱ Время:
    //Нужно перевести число в строку
    //Потом пройти по символам
    //Каждую '1' проверить и прибавить к счётчику
    //Итого: O(log n) по времени и по памяти (вся строка хранится)
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int number = sc.nextInt();
        String binaryString = Integer.toBinaryString(number);

        System.out.println(binaryString + " это его двоичное представление");
        char[] charBinary = binaryString.toCharArray();
        int result = 0;

        //время О(n) Использует дополнительную память (строка и массив символов) Медленнее, создаёт строку
        for (int i = 0; i < charBinary.length; i++) {
            result += Integer.parseInt(String.valueOf(charBinary[i]));
        }
        System.out.println(result + " суммирование двоичного представления");
    }

    public int hammingWeight(int n) {
        //пример
        //n = 10110 (в двоичной системе) = 22     1*2^4 + 0*2^3 + 1*2^2 + 1*2^1 + 0*2^0
        //                                        16    +  0    +  4    +  2    +   0   = 22
        //n        = 10110   (22)
        //n - 1    = 10101   (21)
        //-----------------------
        //n & n-1  = 10100   (20)
        //n        = 10100   (20)
        //n - 1    = 10011   (19)
        //-----------------------
        //n & n-1  = 10000   (16)
        //n        = 10000   (16)
        //n - 1    = 01111   (15)
        //-----------------------
        //n & n-1  = 00000   (0)
        int result = 0;
        //пока n не равно 0
        while (n != 0) {
            // удалили эту '1'
            // Она сравнивает каждый бит числа n и n-1, чтобы удалить младшую единицу
            n = n & (n - 1);
            result++;
        }
        return result;
        // Память:
        //Используются только целые числа (int)
        //Никаких массивов, строк и т. д.
        //➤ O(1) памяти — суперэффективно!
        //⏱ Время:
        //Количество итераций = количество битов '1' в числе
        //(например, у 10110 — 3 единицы → 3 итерации)
        //Поэтому: O(k), где k = число единиц в числе
        //⚡ Обычно работает быстрее, чем со строкой.
    }
}
